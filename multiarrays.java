public class multiarray {
    public static void main(String[] args) throws Exception {



        char[][] Letters = {
            {'A', 'B', 'C'},
            {'D', 'E', 'F'},
            {'G', 'H', 'I'},
        };

        System.out.println(Letters[1][1]);
        System.out.println(Letters[2][0]);
        System.out.println(Letters[0][1]);
    }
}
